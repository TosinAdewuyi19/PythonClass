firstNumber = 1024
secondNumber = 2

if firstNumber % 4 == 0:
    print(f"{firstNumber} is a multiple of 4.")
else:
    print(f"{firstNumber} is not a multiple of 4.")

if secondNumber % 10 == 0:
    print(f"{secondNumber} is a multiple of 10.")
else:
    print(f"{secondNumber} is not a multiple of 10.")